export const translations: Record<string, Partial<Record<SupportedLanguage, string>>> = {
  ...common,
  ...nav,
  ...auth,
  ...admin,
};

export function t(key: string, lang: SupportedLanguage): string {
  const entry = translations[key];
  if (!entry) return key;
  const translation = entry[lang];
  if (!translation) return key;
  return translation;
}

export function getBrandName(lang: SupportedLanguage): string {
  return BRAND_NAME[lang] || 'Babili';
}

export function isRTL(lang: string): boolean {
  return (RTL_LANGUAGES as readonly string[]).includes(lang);
}

export function getDir(lang: string): 'rtl' | 'ltr' {
  return isRTL(lang) ? 'rtl' : 'ltr';
}

