export const ALL_TRANSLATIONS: Record<string, TranslationValue> = {};

function register(
  category: string,
  translations: Record<string, Partial<Record<SupportedLanguage, string>>>,
) {
  for (const [key, value] of Object.entries(translations)) {
    const fullKey = `${category}.${key}`;
    ALL_TRANSLATIONS[fullKey] = {
      key: fullKey,
      category,
      translations: value as Partial<Record<SupportedLanguage, string>>,
    };
  }
}

register('common', commonTranslations);
register('auth', authTranslations);
register('nav', navTranslations);
register('order', orderTranslations);
register('admin', adminTranslations);
register('customer', customerTranslations);
register('error', errorTranslations);

export function getTranslation(key: string, lang: SupportedLanguage): string | undefined {
  const entry = ALL_TRANSLATIONS[key];
  if (!entry) return undefined;
  return entry.translations[lang];
}

export function getTranslationsByCategory(
  category: string,
  lang: SupportedLanguage,
): Record<string, string> {
  const result: Record<string, string> = {};
  for (const [key, entry] of Object.entries(ALL_TRANSLATIONS)) {
    if (entry.category === category && entry.translations[lang]) {
      result[key] = entry.translations[lang]!;
    }
  }
  return result;
}
